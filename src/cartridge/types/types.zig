pub const RomSize = enum(u8) {
    KB_32 = 0x00,
    KB_64 = 0x01,
    KB_128 = 0x02,
    KB_256 = 0x03,
    KB_512 = 0x04,
    MB_1 = 0x05,
    MB_2 = 0x06,
    MB_4 = 0x07,
    MB_8 = 0x08,
    MB_1_1 = 0x52,
    MB_1_2 = 0x53,
    MB_1_5 = 0x54,

    pub fn getRomSize(self: RomSize) usize {
        const value: u8 = @intFromEnum(self);
        if (value <= 0x08) {
            return 32 * 1024 * (1 << value);
        }

        return switch (self) {
            .MB_1_1 => 72 * 16 * 1024,
            .MB_1_2 => 80 * 16 * 1024,
            .MB_1_5 => 96 * 16 * 1024,
        };
    }
};

pub const RamSize = enum(u8) {
    NONE = 0x00,
    UNUSED = 0x01,
    KB_8 = 0x02,
    KB_32 = 0x03,
    KB_128 = 0x04,
    KB_64 = 0x05,

    pub fn getRamSize(self: RamSize) usize {
        return switch (self) {
            .NONE => 0,
            .UNUSED => 0,
            .KB_8 => 8 * 1024,
            .KB_32 => 32 * 1024,
            .KB_128 => 128 * 1024,
            .KB_64 => 64 * 1024,
        };
    }
};
